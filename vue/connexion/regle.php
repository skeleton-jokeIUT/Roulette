    		<h1>Les règles de la roulette</h1>

    		<p>La roulette est un jeu de hasard. Elle comporte 36 cases numérotées de 1 à 36, une bille est lancée dedans et s'arrête à un moment dans une case. Le but est de prévoir où la bille va s'arrêter en misant sur le résultat. Il existe plusieurs façon de miser offrant plus ou moins de chance de gagner et les gains s’accordent avec le pourcentage de chance de gain.
    		</p>

    		<p>
    			Avant chaque partie il faut miser de l’argent selon un choix de jeu. Vous avez les choix suivants :<br>
			-	Miser sur la valeur exacte du résultat<br>
			-	Miser sur la parité du résultat
			</p>
    		
    		<p>
    			En fonction de votre choix, deux gains sont possibles:<br>
				-	35 fois la mise si vous trouvez la valeur exacte<br>
				-	4 fois la mise si vous trouvez la bonne parité
			</p>



    	</section>